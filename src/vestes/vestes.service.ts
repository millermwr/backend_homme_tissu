import { Injectable, NotFoundException, OnModuleInit } from '@nestjs/common';
import { DatabaseService } from '../database/database.service';
import { unlink } from 'node:fs/promises';
import path from 'node:path';
import { getUploadsDir } from '../storage/uploads-path';

interface CreateVesteDto {
  titre: string;
  description: string;
}

interface UpdateVesteDto {
  titre: string;
  description: string;
}

interface VesteRecord {
  id: number;
  titre: string;
  description: string;
  isPublished: boolean;
  createdAt: string;
  updatedAt: string;
}

interface VesteImageRecord {
  id: number;
  vesteId: number;
  mediaUrl: string;
  mediaType: string;
  fileSize: number;
  createdAt: string;
  updatedAt: string;
}

interface VesteWithImagesRecord extends VesteRecord {
  images: VesteImageRecord[];
}

@Injectable()
export class VestesService implements OnModuleInit {
  constructor(private readonly db: DatabaseService) {}

  async onModuleInit() {
    await this.db.execute(`
      CREATE TABLE IF NOT EXISTS Veste (
        id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        titre TEXT NOT NULL,
        description TEXT NOT NULL,
        isPublished BOOLEAN NOT NULL DEFAULT false,
        createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
        updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
      )
    `);

    await this.db.execute(`
      CREATE TABLE IF NOT EXISTS VesteImage (
        id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        vesteId INT NOT NULL,
        mediaUrl TEXT NOT NULL,
        mediaType TEXT NOT NULL,
        fileSize INT NOT NULL DEFAULT 0,
        createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
        updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
        CONSTRAINT fk_veste_image_veste
          FOREIGN KEY (vesteId) REFERENCES Veste(id) ON DELETE CASCADE
      )
    `);

    await this.db.execute(`
      CREATE INDEX IF NOT EXISTS idx_veste_image_vesteId ON VesteImage(vesteId)
    `);
  }

  private async withImages(veste: VesteRecord): Promise<VesteWithImagesRecord> {
    const images = await this.db.query<VesteImageRecord>(
      'SELECT * FROM VesteImage WHERE vesteId = ? ORDER BY createdAt ASC',
      [veste.id],
    );

    return { ...veste, images };
  }

  async create(dto: CreateVesteDto): Promise<VesteWithImagesRecord> {
    await this.db.execute(
      'INSERT INTO Veste (titre, description, isPublished, createdAt, updatedAt) VALUES (?, ?, false, NOW(), NOW())',
      [dto.titre, dto.description],
    );

    const rows = await this.db.query<VesteRecord>(
      'SELECT * FROM Veste ORDER BY id DESC LIMIT 1',
    );

    return this.withImages(rows[0]);
  }

  async addImage(
    vesteId: number,
    mediaUrl: string,
    mediaType: string,
    fileSize: number,
  ) {
    const vesteRows = await this.db.query<{ id: number }>(
      'SELECT id FROM Veste WHERE id = ? LIMIT 1',
      [vesteId],
    );
    if (!vesteRows[0]) {
      throw new NotFoundException('Modele introuvable');
    }

    await this.db.execute(
      'INSERT INTO VesteImage (vesteId, mediaUrl, mediaType, fileSize, createdAt, updatedAt) VALUES (?, ?, ?, ?, NOW(), NOW())',
      [vesteId, mediaUrl, mediaType, fileSize],
    );

    return this.getById(vesteId);
  }

  async removeImage(imageId: number) {
    const imageRows = await this.db.query<{ id: number; mediaUrl: string }>(
      'SELECT id, mediaUrl FROM VesteImage WHERE id = ? LIMIT 1',
      [imageId],
    );
    const image = imageRows[0];

    if (!image) {
      throw new NotFoundException('Image introuvable');
    }

    await this.db.execute('DELETE FROM VesteImage WHERE id = ?', [imageId]);

    if (image.mediaUrl.startsWith('/uploads/')) {
      const filePath = path.join(
        getUploadsDir(),
        path.basename(image.mediaUrl),
      );
      try {
        await unlink(filePath);
      } catch {
        // Ignore missing file errors.
      }
    }

    return { message: 'Image supprimee' };
  }

  async findPublic(): Promise<VesteWithImagesRecord[]> {
    const rows = await this.db.query<VesteRecord>(
      'SELECT * FROM Veste WHERE isPublished = true ORDER BY createdAt DESC',
    );
    return Promise.all(rows.map((row) => this.withImages(row)));
  }

  async findAdmin(): Promise<VesteWithImagesRecord[]> {
    const rows = await this.db.query<VesteRecord>(
      'SELECT * FROM Veste ORDER BY createdAt DESC',
    );
    return Promise.all(rows.map((row) => this.withImages(row)));
  }

  async getById(id: number): Promise<VesteWithImagesRecord> {
    const rows = await this.db.query<VesteRecord>(
      'SELECT * FROM Veste WHERE id = ? LIMIT 1',
      [id],
    );
    const veste = rows[0];
    if (!veste) {
      throw new NotFoundException('Modele introuvable');
    }

    return this.withImages(veste);
  }

  async setPublished(id: number, isPublished: boolean) {
    const vesteRows = await this.db.query<{ id: number }>(
      'SELECT id FROM Veste WHERE id = ? LIMIT 1',
      [id],
    );
    if (!vesteRows[0]) {
      throw new NotFoundException('Modele introuvable');
    }

    await this.db.execute(
      'UPDATE Veste SET isPublished = ?, updatedAt = NOW() WHERE id = ?',
      [isPublished, id],
    );

    return this.getById(id);
  }

  async update(id: number, dto: UpdateVesteDto) {
    const vesteRows = await this.db.query<{ id: number }>(
      'SELECT id FROM Veste WHERE id = ? LIMIT 1',
      [id],
    );
    if (!vesteRows[0]) {
      throw new NotFoundException('Modele introuvable');
    }

    await this.db.execute(
      'UPDATE Veste SET titre = ?, description = ?, updatedAt = NOW() WHERE id = ?',
      [dto.titre, dto.description, id],
    );

    return this.getById(id);
  }

  async remove(id: number) {
    const vesteRows = await this.db.query<{ id: number }>(
      'SELECT id FROM Veste WHERE id = ? LIMIT 1',
      [id],
    );
    if (!vesteRows[0]) {
      throw new NotFoundException('Modele introuvable');
    }

    const imageRows = await this.db.query<{ mediaUrl: string }>(
      'SELECT mediaUrl FROM VesteImage WHERE vesteId = ?',
      [id],
    );

    await this.db.execute('DELETE FROM Veste WHERE id = ?', [id]);

    for (const image of imageRows) {
      if (image.mediaUrl.startsWith('/uploads/')) {
        const filePath = path.join(
          getUploadsDir(),
          path.basename(image.mediaUrl),
        );
        try {
          await unlink(filePath);
        } catch {
          // Ignore missing file errors.
        }
      }
    }

    return { message: 'Modele supprime' };
  }
}
